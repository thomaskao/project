package com.pcsetting.example;

public class p34 {

	public static void main(String[] args) {
		int num3=10,num4=5;
		System.out.println("num3+num4=" +(num3+num4));
		System.out.println("num3-num4=" +(num3-num4));
		System.out.println("num3*num4=" +(num3*num4));	
		System.out.println("num/num4=" +(num3/num4));	
		System.out.println("num3%num4=" +(num3%num4));	
		
		int a=0,b=0,c=0;
		b=a++;
		c=++a;
		System.out.println("先傳值再+1，b="+b);
		System.out.println("先+1再傳值，c="+c);	
				
	}

}
